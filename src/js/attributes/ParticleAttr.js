/**
 * ==================================================================================
 * Holds all particle attributes
 *
 * ==================================================================================
 **/



class ParticleAttr {

    constructor() {
        this.density = 10;
        this.friction = 0.9;
        this.stiffness = 0.15;
        this.offset = 0;

        this.color = 'white';
    }


    /**
     * ==================================================================================
     * @Methods
     * ==================================================================================
     **/

     init() {
        //
     }


    /**
     * ==================================================================================
     * @Getter/Setter
     * ==================================================================================
     **/

    /**
     * Get available `Particle` colors
     * @return {Array}
     */
    getColors() {
        return [
            '#1abc9c', '#2ecc71', '#3498db', '#9b59b6',
            '#f1c40f', '#f39c12', '#e67e22', '#e74c3c',
            '#ecf0f1'
        ];
    }
}



export const PARTICLEATTR = new ParticleAttr();
export default PARTICLEATTR;